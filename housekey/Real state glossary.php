                  <p class="glossary_definition">There are several ways to ensure you have a good credit report and credit score. One of the most effective is to manage your existing credit in a positive way.</p>

                  <p class="glossary_definition">Ask your lender for suggestions about ways to control the amount of money you owe. Or, you can choose a credit counselor from the list provided on this site. Some lenders may view consumers as a greater risk if they have used most or all of their available credit. Consumers who are considered “overextended” may be viewed this way even if they have made all their debt payments on time.</p>

                  <p class="glossary_definition">Missing a payment on a bill should be avoided, as should late payments on any of your credit obligations. Experiencing a mortgage foreclosure, filing for bankruptcy, or having your vehicle repossessed can also affect your credit score and credit report, limiting your ability to get new credit at a reasonable rate.</p>

