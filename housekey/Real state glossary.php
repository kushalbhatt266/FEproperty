                  <h3>IRA (Individual Retirement Account)</h3>

                  <p class="glossary_definition">A retirement account that allows individuals to make tax-deferred contributions to a personal retirement fund. Individuals can place IRA funds in bank accounts or in other forms of investment such as stocks, bonds, or mutual funds.</p>

