                  <h3>Corporate Relocation</h3>

                  <p class="glossary_definition">Arrangements under which an employer moves an employee to another area as part of the employer’s normal course of business or under which it transfers a substantial part or all of its operations and employees to another area because it is relocating its headquarters or expanding its office capacity.</p>

                  <h3>Cost of Funds Index (COFI)</h3>

                  <p class="glossary_definition">An index that is used to determine interest rate changes for certain adjustable-rate mortgage (ARM) plans. It represents the weighted-average cost of savings, borrowings, and advances of the 11th District members of the Federal Home Loan Bank of San Francisco.</p>

