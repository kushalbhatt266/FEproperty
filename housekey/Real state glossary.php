                  <h2><a name="_N">N</a></h2>

                  <h3>Negative Amortization</h3>

                  <p class="glossary_definition">A gradual increase in mortgage debt that occurs when the monthly payment is not large enough to cover the entire principal and interest due. The amount of the shortfall is added to the remaining balance to create “negative” amortization.</p>

