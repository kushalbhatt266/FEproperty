                  <p class="glossary_definition">One way to think of the mortgage note is that it is a legal “IOU.” Often called the promissory note, it represents your promise to pay the lender according to the agreed upon terms of the loan, including when and where to send your payment.</p>

                  <p class="glossary_definition">The note lists any penalties that will be assessed if you don’t make your monthly mortgage payments. It also warns you that the lender can “call” the loan - demand repayment of the entire loan before the end of the term - if you violate the terms of your mortgage.</p>

