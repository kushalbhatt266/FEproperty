                  <p class="glossary_definition">Since each of the main credit bureaus keeps its own records, you may want to request copies from all three: Trans Union, Equifax, and Experian.</p>

                  <p class="glossary_definition">If you have been turned down for credit because of the information in your credit report, you are entitled to receive a free copy of your report within 60 days of the denial. If you haven’t been denied credit, you can still request a copy of your credit report, usually for a nominal fee.</p>

                  <p class="glossary_definition">If you find errors in your report, follow the directions in the credit report and contact the agencies to have the errors corrected. They will investigate the targeted items and remove incorrect information.</p>

                  <p class="glossary_definition">You don’t have to delay applying for a mortgage while errors in your report are being corrected. Explain the discrepancies in the report to your lender and state that the credit agency is correcting them.</p>

                  <h3>Escrow</h3>

                  <p class="glossary_definition">An item of value, money, or documents deposited with a third party to be delivered upon the fulfillment of a condition. For example, the deposit by a borrower with the lender of funds to pay taxes and insurance premiums when they become due, or the deposit of funds or documents with an attorney or escrow agent to be disbursed upon the closing of a sale of real estate.</p>

                  <h3>Escrow Account</h3>

                  <p class="glossary_definition">The account in which a mortgage servicer holds the borrower’s escrow payments prior to paying property expenses.</p>

                  <p class="glossary_definition">An escrow account is money that is deposited with a third party - outside the buyer and the seller - to be used to pay various fees. A borrower typically provides funds that will pay taxes, mortgage insurance, lease payments, hazard insurance premiums, and other payments when they are due.</p>

                  <p class="glossary_definition">An escrow payment by the holder of a mortgage is also known as “impounds” or “reserves” in some states.</p>

                  <p class="glossary_definition">When escrow funds are used to pay taxes, hazard insurance, and other fees, it is called an escrow disbursement. Periodically, an escrow analysis will be performed to determine if current monthly deposits provide sufficient funds to pay bills when they are due.</p>

                  <h3>Escrow Analysis</h3>

                  <p class="glossary_definition">The periodic examination of escrow accounts to determine if current monthly deposits will provide sufficient funds to pay taxes, insurance, and other bills when due.</p>

                  <h3>Escrow Collections</h3>

                  <p class="glossary_definition">Funds collected by the servicer and set aside in an escrow account to pay the borrower’s property taxes, mortgage insurance, and hazard insurance.</p>

                  <h3>Escrow Disbursements</h3>

                  <p class="glossary_definition">The use of escrow funds to pay real estate taxes, hazard insurance, mortgage insurance, and other property expenses as they become due.</p>

                  <h3>Escrow Payment</h3>

                  <p class="glossary_definition">The portion of a mortgagor’s monthly payment that is held by the servicer to pay for taxes, hazard insurance, mortgage insurance, lease payments, and other items as they become due. Known as “impounds” or “reserves” in some states.</p>

                  <h3>Establishing a Credit Report</h3>

                  <p class="glossary_definition">It is possible to establish a credit history even if you do not have a traditional credit record that shows credit card payments or payments on a student or car loan.</p>

                  <p class="glossary_definition">You can build a nontraditional credit history, for example, by documenting your monthly payments to previous and current landlords; to utility companies for your gas, water and telephone services; and to insurance companies for medical, life, and automobile coverage.</p>

                  <p class="glossary_definition">Your lender can provide further details on how you can effectively establish a credit record.</p>

