                  <p class="glossary_definition">A Home Equity Conversion Mortgage (HECM) is a type of home loan that lets homeowners aged 62 or over with little or no remaining balance on their mortgage convert their equity into cash. The equity can be paid to the homeowner in a lump sum, in a stream of payments, draws from a line of credit, or a combination of monthly payments and line of credit.</p>

