                  <p class="glossary_definition">One of the terms you’re likely to hear when you talk about a mortgage with your lender is principal. The principal is the amount originally borrowed or the amount that remains to be paid once you have started making payments. It is also the part of the monthly mortgage payment that reduces the remaining balance of a mortgage.</p>

