                  <h2><a name="_O">O</a></h2>

                  <h3>Occupancy Date</h3>

                  <p class="glossary_definition">This provision is a good way to help ensure that your home will be ready for occupancy after the closing takes place. As part of your formal purchase offer, consider including a provision that holds the seller responsible for paying you rent should they not move out on or prior to the agreed-upon date. This allows you, for example, to use the money you receive to pay your own rent if you are leasing your current residence.</p>

                  <h3>Offer</h3>

                  <p class="glossary_definition">When you make an offer on a house, it means you are making a formal bid to buy a home. You can work with your real estate sales professional to put together a written bid that abides by the laws in your state. Your offer should include such aspects as the address of the home, the sales price, the type of mortgage financing you will use to purchase the home, any personal property that might be included as part of the sale, and a target date for closing and occupancy. An earnest money deposit typically accompanies the offer. Your real estate sales professional can provide guidance on other elements of the offer.</p>

                  <p class="glossary_definition">Once you have made an offer, the seller has the opportunity to accept, decline, or make a counter-offer. If your offer is accepted, you have a ratified sales contract. This contract is the starting point for working with an approved lender to get the mortgage that’s right for you.</p>

                  <h3>One-Year Adjustable-Rate Mortgage</h3>

                  <p class="glossary_definition">This adjustable-rate mortgage (ARM) offers a low initial interest rate with an interest rate that adjusts annually after the first year. The rate cap per annual adjustment is usually 2 percent; the lifetime adjustment caps can be 5 percent or 6 percent. This type of mortgage may be right for you if you anticipate a rapid increase in income over the first few years of your mortgage. That’s because it lets you maximize your purchasing power immediately. It may also be the right mortgage for you if you plan to live in your home for only a few years.</p>

                  <p class="glossary_definition">Advantages:</p>

                  <ul>
                     <li>Maximizes your buying power immediately, especially if you expect your income to rise quickly in the next few years.</li>
                     <li>A low first-year interest rate and a 2 percent annual rate cap.</li>
                     <li>Some one-year ARMs let you convert to a fixed-rate loan at certain adjustment intervals.</li>
                  </ul>

                  <p class="glossary_definition">Ask your approved lender which of their one-year ARMs include this option. Generally, conversions to fixed-rate mortgages are allowed at the third, fourth, or fifth interest rate adjustment dates.</p>

                  <p class="glossary_definition">Details:</p>

                  <ul>
                     <li>You can get a one-year ARM with a term from 10 to 30 years. The most typical ones are 10, 15, or 30 years.</li>
                     <li>The one-year ARM is most often indexed to the weekly average yield of U.S. Treasury securities adjusted to a constant maturity of one year.</li>
                     <li>Can be used to buy one-family, principal residences, including condos, and planned unit developments.</li>
                     <li>Manufactured homes are also eligible. (Manufactured housing units must be built on a permanent chassis at a factory and then transported to a permanent site and attached to a foundation.)</li>
                  </ul>

                  <h3>Ongoing Costs</h3>

                  <p class="glossary_definition">Home buyers should not forget that there are on-going costs associated with owning a home. They include, but are not limited to:</p>

                  <ul>
                     <li>Monthly mortgage payment</li>
                     <li>Mortgage insurance</li>
                     <li>Homeowner’s insurance</li>
                     <li>Property taxes</li>
                     <li>Utilities, such as gas, oil, water and electricity</li>
                  </ul>

                  <p class="glossary_definition">Another cost home buyers should consider is how much it will cost to maintain their home. These costs include everything from cleaning and minor repairs to yard work and painting.</p>

                  <p class="glossary_definition">Condominium owners and people living in planned unit developments should factor in any homeowners’ association fees or similar costs.</p>

                  <h3>Original Principal Balance</h3>

                  <p class="glossary_definition">The total amount of principal owed on a mortgage before any payments are made.</p>

                  <h3>Origination Fee</h3>

                  <p class="glossary_definition">A fee paid to a lender for processing a loan application. The origination fee is stated in the form of points. One point is 1 percent of the mortgage amount.</p>

                  <p class="glossary_definition">The loan origination fee covers the administrative costs of processing the loan. It is often expressed in points. One point is 1 percent of the mortgage amount. For example, a $100,000 mortgage with a loan origination fee of 1 point would mean you pay $1,000.</p>

                  <h3>Other Buyer Costs</h3>

                  <p class="glossary_definition">There are other costs associated with the closing that are typically paid by the buyer. They often include:</p>

                  <ul>
                     <li>Fees paid to the lender: Loan discount points, loan origination fee, credit report fee, appraisal fee, and assumption fee.</li>
                     <li>Advance payments or prepaid fees: Interest, mortgage insurance premium, and hazard insurance premium.</li>
                     <li>Escrow accounts or reserves: State and local law and lenders’ policies vary but these reserves may have to be set up if the lender will be paying property taxes, mortgage insurance, and hazard insurance.</li>
                     <li>Title charges: Closing (or settlement) fee, title insurance premium, title search, document preparation fees, and attorney fees. The fees the buyer pays for a real estate attorney are not part of settlement procedures.</li>
                     <li>Recording and transfer fees: States often impose a tax on the transfer of property. The payment of a fee for recording the purchasing documents may be required.</li>
                     <li>Additional charges: Surveyor’s fees, termite and other pet infestation inspection fees, and the cost of other inspections required by the lender.</li>
                     <li>Adjustments: Items paid by the seller in advance and items yet to be paid for which the seller is responsible. The most common expense is property taxes, but others may have to be addressed.</li>
                  </ul>

                  <h3>Other Contingencies</h3>

                  <p class="glossary_definition">A contingency in a contract states that if a certain requirement is not met, the deal can be canceled. Some of the most common contingencies related to home purchases include:</p>

                  <ul>
                     <li>Professional home inspection: This states that your sales contract is contingent on a satisfactory report by a professional home inspector. You have the right not to proceed with the purchase of the home, or to re-negotiate the terms of purchase, if any major problems are uncovered.</li>
                     <li>Termite inspection: This states that the property is free of both visible termite infestation and termite damage.</li>
                     <li>Asbestos: You may choose to hire a qualified professional to inspect the home, take samples for asbestos, and offer solutions to correct any problems.</li>
                     <li>Formaldehyde: This colorless, gas chemical was used in foam insulation for homes until the early 1980s and is emitted by some construction materials. It is suspected of causing cancer, and it can also irritate the throat, nose, and eyes. A qualified inspector can let you know if the gas is present in the home you wish to purchase.</li>
                     <li>Radon: Most home buyers require that the house be tested for radon, a naturally occurring, odorless gas that can cause health problems.</li>
                     <li>Hazardous waste sites: The Environmental Protection Agency has identified contaminated hazardous waste sites across the country. You can contact your EPA regional office for more information.</li>
                     <li>Lead-based paint: You should also have the house inspected for lead-based paint, which can lead to very serious health problems. If the house was built before 1950, you can be fairly certain lead-based paint was used. For houses built between 1950 and 1978, there is also a chance lead-based paint was used. Lead disclosure regulations can vary from state to state. Health officials in the state where the home you want to buy is located may be able to provide further guidance.</li>
                  </ul>

                  <p class="glossary_definition">The seller or real estate professional must give you a pamphlet that explains lead hazards and tell you about any lead-based paint of which the seller is aware before a sales contract on a home built before 1978 can be finalized. The seller must also allow 10 days during which you can hire a professional to conduct an inspection for lead-based paint hazards.</p>

                  <h3>Other Financial Companies</h3>

                  <p class="glossary_definition">Other financial companies include credit unions, mortgage brokers, insurance companies, investment bankers, and housing finance agencies.</p>

                  <p class="glossary_definition">Credit unions are cooperative, not-for-profit institutions organized to promote savings and to provide credit, including mortgage loans, to their members. Credit unions either service the mortgages they originate or sell them to other investors.</p>

                  <p>Mortgage brokers are independent real estate financing professionals who specialize in the origination of residential and/or commercial mortgages. Mortgage brokers originate loans on behalf of other lenders -- including banks, thrifts and mortgage banking companies, but do not service loans.</p>

                  <p class="glossary_definition">Insurance companies and investment bankers are large institutional investors in mortgages that do not receive deposits from consumers. They use premiums from their clients’ insurance polices and investment packages to fund their mortgage lending activities.</p>

                  <p class="glossary_definition">Housing finance agencies are typically associated with state or local governments. They are generally geared toward assisting first-time and low- to moderate-income borrowers. They use tax exempt bonds to fund mortgage lending and as a result are often able to provide interest rates that are below current market rates.</p>

