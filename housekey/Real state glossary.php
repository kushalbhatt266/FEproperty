                  <h2><a name="_A">A</a></h2>

                  <h3>Acceleration Clause</h3>

                  <p class="glossary_definition">A provision in a mortgage that gives the lender the right to demand payment of the entire principal balance if a monthly payment is missed.</p>

                  <h3>Acceptance</h3>

                  <p class="glossary_definition">An offeree’s consent to enter into a contract and be bound by the terms of the offer.</p>

                  <h3>Additional Principal Payment</h3>

                  <p class="glossary_definition">A payment by a borrower of more than the scheduled principal amount due in order to reduce the remaining balance on the loan.</p>

                  <h3>Adjustable Rate Mortgage (ARM)</h3>

                  <p class="glossary_definition">A mortgage that permits the lender to adjust its interest rate periodically on the basis of changes in a specified index.</p>

                  <h3>Adjusted Basis</h3>

                  <p class="glossary_definition">The original cost of a property plus the value of any capital expenditures for improvements to the property minus any depreciation taken.</p>

                  <h3>Adjustment Date</h3>

                  <p class="glossary_definition">The date on which the interest rate changes for an adjustable-rate mortgage (ARM).</p>

                  <h3>Adjustment Period</h3>

                  <p class="glossary_definition">The period that elapses between the adjustment dates for an adjustable-rate mortgage (ARM).</p>

                  <h3>Administrator</h3>

                  <p class="glossary_definition">A person appointed by a probate court to administer the estate of a person who died intestate.</p>

                  <h3>Affidavits</h3>

                  <p class="glossary_definition">A formal sworn statement of fact. As part of the closing process, you’re likely to sign numerous affidavits. You may be required, for example, to sign an affidavit of occupancy. It states that you will use the property as a principal residence. Or, you and the seller may have to sign an affidavit stating all of the improvements to the property required in the sales contract were completed before closing.</p>

                  <p class="glossary_definition">Your lender can provide additional information regarding any of these documents you will sign.</p>

                  <h3>Affordability Analysis</h3>

                  <p class="glossary_definition">A detailed analysis of your ability to afford the purchase of a home. An affordability analysis takes into consideration your income, liabilities, and available funds, along with the type of mortgage you plan to use, the area where you want to purchase a home, and the closing costs that you might expect to pay.</p>

                  <h3>Amenity</h3>

                  <p class="glossary_definition">A feature of real property that enhances its attractiveness and increases the occupant’s or user’s satisfaction although the feature is not essential to the property’s use. Natural amenities include a pleasant or desirable location near water, scenic views of the surrounding area, etc. Human-made amenities include swimming pools, tennis courts, community buildings, and other recreational facilities.</p>

                  <h3>Amortization</h3>

                  <p class="glossary_definition">The gradual repayment of a mortgage loan by installments.</p>

                  <h3>Amortization Term</h3>

                  <p class="glossary_definition">The amount of time required to amortize the mortgage loan. The amortization term is expressed as a number of months. For example, for a 30-year fixed-rate mortgage, the amortization term is 360 months.</p>

                  <h3>Amortize</h3>

                  <p class="glossary_definition">To repay a mortgage with regular payments that cover both principal and interest.</p>

                  <h3>Amotization Schedule</h3>

                  <p class="glossary_definition">A timetable for payment of a mortgage loan. An amortization schedule shows the amount of each payment applied to interest and principal and shows the remaining balance after each payment is made.</p>

                  <h3>Annual Mortgagor Statement</h3>

                  <p class="glossary_definition">A report sent to the mortgagor each year. The report shows how much was paid in taxes and interest during the year, as well as the remaining mortgage loan balance at the end of the year.</p>

                  <h3>Annual Percentage Rate (APR)</h3>

                  <p class="glossary_definition">The cost of a mortgage stated as a yearly rate; includes such items as interest, mortgage insurance, and loan origination fee (points).</p>

                  <h3>Annuity</h3>

                  <p class="glossary_definition">An amount paid yearly or at other regular intervals, often on a guaranteed dollar basis.</p>

                  <h3>Application</h3>

                  <p class="glossary_definition">A form used to apply for a mortgage loan and to record pertinent information concerning a prospective mortgagor and the proposed security.</p>

                  <p class="glossary_notes">*See also “Loan Application” entry.</p>

                  <h3>Appraisal</h3>

                  <p class="glossary_definition">A written analysis of the estimated value of a property prepared by a qualified appraiser. Contrast with home inspection.</p>

                  <h3>Appraised Value</h3>

                  <p class="glossary_definition">An opinion of a property’s fair market value, based on an appraiser’s knowledge, experience, and analysis of the property.</p>

                  <h3>Appraiser</h3>

                  <p class="glossary_definition">A person qualified by education, training, and experience to estimate the value of real property and personal property.</p>

                  <h3>Appreciation</h3>

                  <p class="glossary_definition">An increase in the value of a property due to changes in market conditions or other causes. The opposite of depreciation.</p>

                  <h3>Assessed Value</h3>

                  <p class="glossary_definition">The valuation placed on property by a public tax assessor for purposes of taxation.</p>

                  <h3>Assessment</h3>

                  <p class="glossary_definition">The process of placing a value on property for the strict purpose of taxation. May also refer to a levy against property for a special purpose, such as a sewer assessment.</p>

                  <h3>Assessment Rolls</h3>

                  <p class="glossary_definition">The public record of taxable property.</p>

                  <h3>Assessor</h3>

                  <p class="glossary_definition">A public official who establishes the value of a property for taxation purposes.</p>

                  <h3>Asset</h3>

                  <p class="glossary_definition">Anything of monetary value that is owned by a person. Assets include real property, personal property, and enforceable claims against others (including bank accounts, stocks, mutual funds, and so on).</p>

                  <h3>Assignment</h3>

                  <p class="glossary_definition">The transfer of a mortgage from one person to another.</p>

                  <h3>Assumable Mortgage</h3>

                  <p class="glossary_definition">A mortgage that can be taken over (“assumed”) by the buyer when a home is sold.</p>

                  <p class="glossary_definition">A provision in an assumable mortgage allows a buyer to assume responsibility for the mortgage from the seller. The loan does not need to be paid in full by the original borrower upon the sale or transfer of the property.</p>

                  <h3>Assumption</h3>

                  <p class="glossary_definition">The transfer of the seller’s existing mortgage to the buyer.</p>

                  <p class="glossary_notes">*See also “Assumable Mortgage” entry.</p>

                  <h3>Assumption Clause</h3>

                  <p class="glossary_definition">A provision in an assumable mortgage that allows a buyer to assume responsibility for the mortgage from the seller. The loan does not need to be paid in full by the original borrower upon sale or transfer of the property.</p>

                  <h3>Assumption Fee</h3>

                  <p class="glossary_definition">The fee paid to a lender (usually by the purchaser of real property) resulting from the assumption of an existing mortgage.</p>

                  <h3>Attorney-In-Fact</h3>

                  <p class="glossary_definition">One who holds a power of attorney from another to execute documents on behalf of the grantor of the power.</p>

                  <h3>Automated Underwriting</h3>

                  <p class="glossary_definition">After you complete your loan application with a lender, it is sent to “underwriting” for review. In short, underwriting is the process used to analyze how you have managed credit obligations in the past, whether you have the ability to repay the mortgage loan you are applying for (i.e., your income and assets), and whether the price you are willing to pay for the home is supported by the price of the property.</p>

