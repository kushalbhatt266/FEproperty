                  <h2><a name="_M">M</a></h2>

                  <h3>Manufactured Housing</h3>

                  <p class="glossary_definition">Homes and dwellings that are not built at the home site and are moved to the location are considered manufactured housing. Manufactured housing units must be built on a permanent chassis at a factory and then transported to a permanent site and attached to a foundation. All manufactured homes must be built to meet standards set forth by the U.S. Department of Housing and Urban Development (HUD). The standards focus on such aspects as design, strength, energy efficiency, and fire resistance.</p>

                  <p class="glossary_definition">Manufactured housing represents one of the fastest-growing housing markets in the United States. Nearly all of the mortgage products are available for owners of manufactured housing.</p>

                  <h3>Margin</h3>

                  <p class="glossary_definition">For an adjustable-rate mortgage (ARM), the amount that is added to the index to establish the interest rate on each adjustment date, subject to any limitations on the interest rate change.</p>

                  <h3>Market Value</h3>

                  <p class="glossary_definition">You can get a good feel for the market value of a home by asking whether the listing agent compiled a “comparative market analysis (CMA)”. This written report on the property examines comparable homes in the area that have recently been sold, are currently on the market, or are currently under contract.</p>

                  <p class="glossary_definition">The CMA will help you figure out whether the asking price is in line with other comparable houses in the neighborhood.</p>

                  <h3>Master Association</h3>

                  <p class="glossary_definition">A homeowners’ association in a large condominium or planned unit development (PUD) project that is made up of representatives from associations covering specific areas within the project. In effect, it is a “second-level” association that handles matters affecting the entire development, while the “first-level” associations handle matters affecting their particular portions of the project.</p>

                  <h3>Maturity</h3>

                  <p class="glossary_definition">The date on which the principal balance of a loan, bond, or other financial instrument becomes due and payable.</p>

                  <h3>Maximum Claim Amount</h3>

                  <p class="glossary_definition">Your maximum claim amount is the lesser of two figures:</p>

                  <ul>
                     <li>Your home’s appraised value.</li>
                     <li>HUD 203(b) limit.</li>
                  </ul>

                  <p class="glossary_definition">The HUD 203(b) limit is the maximum loan amount that FHA will insure for residences in your geographical area. Check with your lender to get the latest figures for your area.</p>

                  <h3>Maximum Financing</h3>

                  <p class="glossary_definition">A mortgage amount that is within 5 percent of the highest loan-to-value (LTV) percentage allowed for a specific product. Thus, maximum financing on a fixed-rate mortgage would be 90 percent or higher, because 95 percent is the maximum allowable LTV percentage for that product.</p>

                  <h3>Merged Credit Report</h3>

                  <p class="glossary_definition">A credit report that contains information from three credit repositories. When the report is created, the information is compared for duplicate entries. Any duplicates are combined to provide a summary of a your credit.</p>

                  <h3>Modification</h3>

                  <p class="glossary_definition">The act of changing any of the terms of the mortgage.</p>

                  <h3>Money Market Account</h3>

                  <p class="glossary_definition">A savings account that provides bank depositors with many of the advantages of a money market fund. Certain regulatory restrictions apply to the withdrawal of funds from a money market account.</p>

                  <h3>Money Market Fund</h3>

                  <p class="glossary_definition">A mutual fund that allows individuals to participate in managed investments in short-term debt securities, such as certificates of deposit and Treasury bills.</p>

                  <h3>Monthly Fixed Installment</h3>

                  <p class="glossary_definition">That portion of the total monthly payment that is applied toward principal and interest. When a mortgage negatively amortizes, the monthly fixed installment does not include any amount for principal reduction.</p>

                  <h3>Monthly Payment Mortgage</h3>

                  <p class="glossary_definition">A mortgage that requires payments to reduce the debt once a month.</p>

                  <p class="glossary_definition">Your monthly mortgage payment is composed of four components.</p>

                  <ul>
                     <li>Principal refers to the part of the monthly payment that reduces the remaining balance of the mortgage.</li>
                     <li>Interest is the fee charged for borrowing money.</li>
                     <li>Taxes and insurance refer to the amounts that are paid into an escrow account each month for property taxes and mortgage and hazard insurance.</li>
                  </ul>

                  <p class="glossary_definition">All four of these elements are often referred to as PITI.</p>

                  <p class="glossary_definition">Your monthly mortgage payment due may be mailed to you in a book of coupons each year, or in a separate coupon every month.</p>

                  <p class="glossary_definition">Ask your lender if the automated underwriting system is used, which may reduce costs associated with your mortgage.</p>

                  <h3>Mortgage</h3>

                  <p class="glossary_definition">A legal document that pledges a property to the lender as security for payment of a debt.</p>

                  <p class="glossary_definition">Simply put, the mortgage is the legal document that gives the lender a legal claim against your house should you default on your loan payments. The mortgage indicates that a specific amount of money will be loaned at a specific interest rate so that you can buy your home. Another way of thinking of the mortgage is that you have possession of the property but the lender has ownership until you have repaid your loan.</p>

                  <p class="glossary_definition">The items stated in the mortgage include the homeowner’s responsibility to:</p>

                  <ul>
                     <li>pay principal</li>
                     <li>pay interest</li>
                     <li>pay taxes</li>
                     <li>pay insurance on time</li>
                     <li>pay to maintain hazard insurance on the property</li>
                     <li>adequately maintain the property</li>
                  </ul>

                  <p class="glossary_definition">The mortgage also includes the basic information found in the note.</p>

                  <p class="glossary_definition">Should you consistently fail to meet these requirements, your lender can seek full repayment of the balance of the loan, foreclose on the property, or sell the property and use the proceeds to pay off the loan balance and foreclosure costs.</p>

                  <p class="glossary_definition">A deed of trust is used instead of a mortgage in some states.</p>

                  <h3>Mortgage Banker</h3>

                  <p class="glossary_definition">A company that originates mortgages exclusively for resale in the secondary mortgage market.</p>

                  <p class="glossary_definition">Mortgage companies originate and service mortgages. In other words, they make loans to consumers. Mortgage companies then typically sell these loans to other lenders and investors.</p>

                  <p class="glossary_definition">Some mortgage companies may be subsidiaries of depository institutions or their holding companies but do not receive money from individual depositors.</p>

                  <h3>Mortgage Broker</h3>

                  <p class="glossary_definition">An individual or company that brings borrowers and lenders together for the purpose of loan origination. Mortgage brokers typically require a fee or a commission for their services.</p>

                  <p class="glossary_definition">The National Association of Mortgage Brokers defines a mortgage broker as “an independent real estate financing professional who specializes in the origination of residential and/or commercial mortgages.”</p>

                  <p class="glossary_definition">There are an estimated 20,000 mortgage brokerage operations from coast to coast. They originate more than half of the residential loans in the U.S.</p>

                  <p class="glossary_definition">A mortgage broker has professional expertise that can assist mortgage seekers in finding the best loan for them. The mortgage broker is also experienced in offering many applicable financing options for a consumer’s specific needs.</p>

                  <h3>Mortgage Insurance</h3>

                  <p class="glossary_definition">A contract that insures the lender against loss caused by a mortgagor’s default on a government mortgage or conventional mortgage. Mortgage insurance can be issued by a private company or by a government agency such as the Federal Housing Administration (FHA). Depending on the type of mortgage insurance, the insurance may cover a percentage of or virtually all of the mortgage loan.</p>

                  <h3>Mortgage Insurance Premium (MIP)</h3>

                  <p class="glossary_definition">The amount paid by a mortgagor for mortgage insurance, either to a government agency such as the Federal Housing Administration (FHA) or to a private mortgage insurance (MI) company.</p>

                  <h3>Mortgage Life Insurance</h3>

                  <p class="glossary_definition">A type of term life insurance often bought by mortgagors. The amount of coverage decreases as the principal balance declines. In the event that the borrower dies while the policy is in force, the debt is automatically satisfied by insurance proceeds.</p>

                  <h3>Mortgage-Related Closing Costs</h3>

                  <p class="glossary_definition">Mortgage-related closing costs generally are costs associated with your loan application. They vary, but here are some of the most common ones:</p>

                  <ul>
                     <li>Loan origination fee: This fee covers the administrative costs of processing the loan. It may be expressed as a percentage of the loan (for example, 1 percent of the mortgage amount).</li>
                     <li>Loan discount points: These points are additional funds you pay the lender at closing to get a lower interest rate on your mortgage. Typically, each point you pay for a 30-year loan lowers your interest rate by .125 of a percentage point. If the current interest rate on a no-point, 30-year mortgage is 7.75 percent, paying one point would lower the interest rate to 7.625. Each point is one percent of the mortgage (for example, if your mortgage is $200,000, one point equals $2,000).</li>
                     <li>Appraisal fee: This fee pays for the appraisal, which the lender uses to determine whether the value of the property secures the loan should you default. The home buyer usually pays this fee. It may appear on the settlement form as “POC,” or “paid outside closing.”</li>
                     <li>Credit report fee: This covers the cost of the credit report, which the lender uses to determine your creditworthiness.</li>
                     <li>Assumption fee: This fee is charged if you take over the payments on the seller’s existing loan. It may range from hundreds of dollars to one percent of the loan amount.</li>
                     <li>Prepaid interest: You are charged interest when you borrow money from a lender, and you will pay interest on the mortgage amount from the date of settlement to the beginning of the period covered by the first monthly mortgage payment. At closing, you may be required to pay in advance the interest for the period.</li>
                     <li>Escrow accounts: Also called reserves, these accounts are required if your lender will be paying your homeowner’s insurance and property taxes. Your lender sets up the escrow account by adding the cost of the insurance and taxes to your monthly mortgage payments. It is kept in reserve until the bills are due. The bills are sent directly to your lender, who makes the payments for you.</li>
                  </ul>

