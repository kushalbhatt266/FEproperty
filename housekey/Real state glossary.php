                  <p class="glossary_definition">Buyers and sellers often negotiate who will pay certain closing costs, and the results vary depending on the negotiated deal. In fact, it’s not uncommon for a sales agreement to state that either the buyer or seller pays all closing costs. The agreement that you and the seller reach must be specified in the sales contract.</p>

