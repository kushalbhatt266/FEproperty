                  <h2><a name="_B">B</a></h2>

                  <h3>Balance Sheet</h3>

                  <p class="glossary_definition">A financial statement that shows assets, liabilities, and net worth as of a specific date.</p>

                  <h3>Balloon Mortgage</h3>

                  <p class="glossary_definition">A mortgage that has level monthly payments that will amortize it over a stated term but that provides for a lump sum payment to be due at the end of an earlier specified term.</p>

                  <h3>Balloon Payment</h3>

                  <p class="glossary_definition">The final lump sum payment that is made at the maturity date of a balloon mortgage.</p>

                  <h3>Bankrupt</h3>

                  <p class="glossary_definition">A person, firm, or corporation that, through a court proceeding, is relieved from the payment of all debts after the surrender of all assets to a court-appointed trustee.</p>

                  <h3>Bankruptcy</h3>

                  <p class="glossary_definition">A proceeding in a federal court in which a debtor who owes more than his or her assets can relieve the debts by transferring his or her assets to a trustee.</p>

                  <h3>Before-Tax Income</h3>

                  <p class="glossary_definition">Income before taxes are deducted.</p>

                  <h3>Beneficiary</h3>

                  <p class="glossary_definition">The person designated to receive the income from a trust, estate, or a deed of trust.</p>

                  <h3>Bequeath</h3>

                  <p class="glossary_definition">To transfer personal property through a will.</p>

                  <h3>Betterment</h3>

                  <p class="glossary_definition">An improvement that increases property value as distinguished from repairs or replacements that simply maintain value.</p>

                  <h3>Bill of Sale</h3>

                  <p class="glossary_definition">A written document that transfers title to personal property.</p>

                  <h3>Binder</h3>

                  <p class="glossary_definition">A preliminary agreement, secured by the payment of an earnest money deposit, under which a buyer offers to purchase real estate.</p>

                  <h3>Biweekly Mortgages</h3>

                  <p class="glossary_definition">Your lender will probably tell you that a biweekly mortgage is structured just like a traditional fixed-rate, level-payment, fully amortizing mortgage. However, you make your payments every 14 days instead of once a month. The monthly payment is split in half, resulting in the same total monthly mortgage, but the resulting 26 and sometimes 27 biweekly payments a year translate into 13 monthly payments, or one extra monthly payment per year.</p>

                  <p class="glossary_definition">Borrowers can qualify for a 30-year monthly payment amount, but get a loan that pays off in approximately 22 years at current interest rates. At higher rates, the actual term declines.</p>

                  <p class="glossary_definition">If you are looking to build up equity in your home faster without the higher mortgage payments that come with a shorter-term mortgage, you may want to consider the biweekly mortgage. Payments can be deducted from your bank account and scheduled to coincide with your payroll deposits to simplify budgeting. Lenders may charge an initial set-up fee to automatically debit your checking account.</p>

                  <h3>Blanket Insurance Policy</h3>

                  <p class="glossary_definition">A single policy that covers more than one piece of property (or more than one person).</p>

                  <h3>Blanket Mortgage</h3>

                  <p class="glossary_definition">The mortgage that is secured by a cooperative project, as opposed to the share loans on individual units within the project.</p>

                  <h3>Bona Fide</h3>

                  <p class="glossary_definition">In good faith, without fraud.</p>

                  <h3>Bond</h3>

                  <p class="glossary_definition">An interest-bearing certificate of debt with a maturity date. An obligation of a government or business corporation. A real estate bond is a written obligation usually secured by a mortgage or a deed of trust.</p>

                  <h3>Breach</h3>

                  <p class="glossary_definition">A violation of any legal obligation.</p>

                  <h3>Bridge Loan</h3>

                  <p class="glossary_definition">A form of second trust that is collateralized by the borrower’s present home (which is usually for sale) in a manner that allows the proceeds to be used for closing on a new house before the present home is sold. Also known as “swing loan.”</p>

                  <h3>Broker</h3>

                  <p class="glossary_definition">A person who, for a commission or a fee, brings parties together and assists in negotiating contracts between them.</p>

                  <h3>Budget</h3>

                  <p class="glossary_definition">A detailed plan of income and expenses expected over a certain period of time. A budget can provide guidelines for managing future investments and expenses.</p>

                  <h3>Budget Category</h3>

                  <p class="glossary_definition">A category of income or expense data that you can use in a budget. You can also define your own budget categories and add them to some or all of the budgets you create. “Rent” is an example of an expense category. “Salary” is a typical income category.</p>

                  <h3>Building Code</h3>

                  <p class="glossary_definition">Local regulations that control design, construction, and materials used in construction. Building codes are based on safety and health standards.</p>

                  <h3>Buydown Account</h3>

                  <p class="glossary_definition">An account in which funds are held so that they can be applied as part of the monthly mortgage payment as each payment comes due during the period that an interest rate buydown plan is in effect.</p>

                  <h3>Buydown Mortgage</h3>

                  <p class="glossary_definition">A temporary buydown is a mortgage on which an initial lump sum payment is made by any party to reduce a borrower’s monthly payments during the first few years of a mortgage. A permanent buydown reduces the interest rate over the entire life of a mortgage.</p>

