                  <h2><a name="_L">L</a></h2>

                  <h3>Late Charge</h3>

                  <p class="glossary_definition">The penalty a borrower must pay when a payment is made a stated number of days (usually 15) after the due date.</p>

                  <h3>Lease</h3>

                  <p class="glossary_definition">A written agreement between the property owner and a tenant that stipulates the conditions under which the tenant may possess the real estate for a specified period of time and rent.</p>

                  <h3>Lease-Purchase Option</h3>

                  <p class="glossary_definition">An alternative financing option that allows low- and moderate-income home buyers to lease a home from a nonprofit organization with an option to buy. Each month’s rent payment consists of principal, interest, taxes and insurance (PITI) payments on the first mortgage plus an extra amount that is earmarked for deposit to a savings account in which money for a downpayment will accumulate.</p>

                  <p class="glossary_definition">Nonprofit organizations may use the lease-purchase option to purchase a home that they then rent to a consumer, or “leaseholder.” The leaseholder has the option to buy the home after a designated period of time (usually three or five years). Part of each rent payment is put aside toward savings for the purpose of accumulating the down payment and closing costs.</p>

