                  <p class="glossary_definition">Any amount that is paid to reduce the principal balance of a loan before the due date - such as the sale of the property, the owner’s decision to pay the loan in full, the owner’s decision to pay additional money every month to lower the principle or interest - is considered prepayment.</p>

