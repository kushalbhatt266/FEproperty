                  <p class="glossary_definition">The percentage rate at which interest accrues on the mortgage. In most cases, it is also the rate used to calculate the monthly payments, although it is not used for an adjustable-rate mortgage (ARM) with payment change limitations.</p>

                  <h3>Interest Rate</h3>

                  <p class="glossary_definition">The rate of interest in effect for the monthly payment due.</p>

                  <h3>Interest Rate Buydown Plan</h3>

                  <p class="glossary_definition">An arrangement wherein the property seller (or any other party) deposits money to an account so that it can be released each month to reduce the mortgagor’s monthly payments during the early years of a mortgage. During the specified period, the mortgagor’s effective interest rate is “bought down” below the actual interest rate.</p>

                  <h3>Interest Rate Ceiling</h3>

                  <p class="glossary_definition">For an adjustable-rate mortgage (ARM), the maximum interest rate, as specified in the mortgage note.</p>

                  <h3>Interest Rate Floor</h3>

                  <p class="glossary_definition">For an adjustable-rate mortgage (ARM), the minimum interest rate, as specified in the mortgage note.</p>

                  <h3>Interest Rate for HECMs</h3>

                  <p class="glossary_definition">The interest rate on a Home Equity Conversion Mortgage (HECM) adjusts monthly or yearly. It is tied to the weekly average yield of U.S. Treasury securities adjusted to a constant maturity of one year. The interest charged on the HECM loan will be payable to your lender when the loan terminates.</p>

                  <h3>InterestFirstSM Mortgage</h3>

                  <p class="glossary_definition">If you’re looking to leverage your mortgage to expand purchasing power, this mortgage offers the benefit of a low, fixed-rate monthly payment.</p>

                  <p class="glossary_definition">Advantages:</p>

                  <ul>
                     <li>For the first 15 years, monthly payments are lower than a comparable 30-year fixed-rate loan.</li>
                     <li>Gain control of your cash flow.</li>
                     <li>Ideal if you plan to stay in your home no more than 15 years and want the lowest monthly payment for that period.</li>
                     <li>Flexible cash flow for college costs, home improvements, IRA contributions, consumer debt reduction, or optional principal payments.</li>
                  </ul>

                  <p class="glossary_definition">Details:</p>

                  <ul>
                     <li>For the first 15 years, you pay only the interest due every month.</li>
                     <li>Any prepayments will reduce your principal balance and reduce future monthly payments.</li>
                     <li>Prepayment of principal may be made without penalty.</li>
                     <li>Payment adjusts at the start of year 16 to cover all interest and principal due on the loan for the remaining 15 years.</li>
                     <li>Monthly payment is fixed during years 16 through 30.</li>
                  </ul>

