                  <p class="glossary_definition">The RHS is part of the U.S. Department of Agriculture.</p>

                  <h3>Discount points</h3>

                  <p class="glossary_definition">Discount points are often used to describe a type of fee that lenders charge. Discount points are additional funds you pay the lender at closing to get a lower interest rate on your mortgage.</p>

                  <p class="glossary_definition">A point equals 1 percent of the loan amount. So, if you and your lender agree to a mortgage of $100,000, one point would equal $1,000.</p>

                  <p class="glossary_definition">Typically, each point you pay for a 30-year loan lowers your interest rate by .125 of a percentage point. If the current interest rate on a 30-year mortgage is 7.75 percent, paying one point would lower the interest rate to 7.625.</p>

                  <p class="glossary_definition">Ask your lender if you have the option of paying 1, 2, or 3 discount points - or you can choose not to pay any discount points. It often makes more sense to pay discount points if you plan to stay in your home for a long time.</p>

                  <h3>Dower</h3>

                  <p class="glossary_definition">The rights of a widow in the property of her husband at his death.</p>

                  <h3>Down Payment</h3>

                  <p class="glossary_definition">The part of the purchase price of a property that the buyer pays in cash and does not finance with a mortgage.</p>

                  <p class="glossary_definition">Saving for a down payment is usually one of the most difficult parts of preparing to buy a home. If you believe you have the needed funds, you are in a better position to seek pre-qualification from a lender to get the mortgage that is right for you.</p>

                  <p class="glossary_definition">Most homeowners rely on a mortgage from a financial institution, and most mortgage products require buyers to include a portion of their own funds towards the purchase of the home. This is called the down payment. Lenders feel more secure when buyers include a down payment, indicating they are less likely to walk away from their investment if their finances take a downturn.</p>

                  <p class="glossary_definition">Historically, buyers usually made a down payment that totaled 20 percent of the home’s purchase price. Under this scenario, a down payment for a $100,000 home is $20,000. But today, new mortgage products allow buyers to put down as little as 3 percent to 5 percent, provided private mortgage insurance is obtained. The down payment for a $100,000 home with 5 percent down payment is just $5,000.</p>

                  <p class="glossary_definition">Sources for down payments may come from buyers’ savings accounts, checking accounts, stocks and bonds, life insurance policies, and gifts.</p>

