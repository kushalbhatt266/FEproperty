                  <p class="glossary_definition">A mortgage that is guaranteed by the Department of Veterans Affairs (VA).</p>

                  <h3>Vested</h3>

                  <p class="glossary_definition">Having the right to use a portion of a fund such as an individual retirement fund. For example, individuals who are 100 percent vested can withdraw all of the funds that are set aside for them in a retirement fund. However, taxes may be due on any funds that are actually withdrawn.</p>

