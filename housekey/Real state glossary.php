                  <h2><a name="_Q">Q</a></h2>

                  <h3>Qualifying Guidelines</h3>

                  <p class="glossary_definition">There are two main elements lenders consider when determining whether you and any co-borrowers qualify for a specific mortgage.</p>

                  <p class="glossary_definition">The first is your monthly mortgage costs, including mortgage payments, property taxes and insurance. If you’re considering buying a condominium or cooperative, any associated fees are also considered. Your mortgage costs should not exceed 28 percent of your gross monthly (pre-tax) income.</p>

                  <p class="glossary_definition">The second qualifying guideline relates to your total monthly housing costs and other debts you and any co-borrowers have. These costs should not exceed 36 percent of your gross monthly income.</p>

                  <p class="glossary_definition">Lenders follow these guidelines because they believe these percentages allow homeowners to pay off their mortgages fairly comfortably without the worry of loan defaults and foreclosures.</p>

                  <p class="glossary_definition">However, these guidelines can be exceeded in certain cases, such as borrowers with a good credit history or with a larger down payment.</p>

                  <h3>Qualifying Ratios</h3>

                  <p class="glossary_definition">Calculations that are used in determining whether a borrower can qualify for a mortgage. They consist of two separate calculations: a housing expense as a percent of income ratio and total debt obligations as a percent of income ratio.</p>

