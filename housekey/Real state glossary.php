                  <p class="glossary_definition">When figuring out how much home you can afford, you need to account for the costs associated with getting into your home.</p>

                  <p class="glossary_definition">These can include the cost for repairs that need to be made before you can occupy your residence. There may also be the cost of purchasing appliances, such as a washer and dryer, refrigerator, or stove.</p>

                  <p class="glossary_definition">The bottom line is you do not want to spend all your money on purchasing the home and not have any left to pay these types of costs.</p>

