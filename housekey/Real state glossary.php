                  <h3>Contractor</h3>

                  <p class="glossary_definition">A general contractor is a person who oversees a construction project and handles aspects such as scheduling workers and ordering supplies.</p>

                  <h3>Conventional Mortgage</h3>

                  <p class="glossary_definition">A mortgage that is not insured or guaranteed by the federal government. Contrast with government mortgage.</p>

