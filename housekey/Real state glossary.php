                  <p class="glossary_definition">Several closing costs will be paid at this meeting. These expenses are over and above the price of the property and are incurred when ownership of a property is transferred. Closing costs generally include a loan origination fee, an attorney’s fee, taxes, an amount placed in escrow, and charges for obtaining title insurance, and a survey. Closing costs vary according to the area of the country.</p>

