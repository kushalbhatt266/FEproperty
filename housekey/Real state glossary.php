                  <h3>Cooperative Corporation</h3>

                  <p class="glossary_definition">A business trust entity that holds title to a cooperative project and grants occupancy rights to particular apartments or units to shareholders through proprietary leases or similar arrangements. A business trust entity that holds title to a cooperative project and grants occupancy rights to particular apartments or units to shareholders through proprietary leases or similar arrangements.</p>

                  <h3>Cooperative Mortgages</h3>

                  <p class="glossary_definition">Mortgages related to a cooperative project. This usually refers to the multifamily mortgage covering the entire project but occasionally describes the share loans on the individual units.</p>

                  <h3>Cooperative Project</h3>

