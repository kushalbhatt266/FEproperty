                  <p class="glossary_definition">A way of holding title to a property wherein the mortgagor does not actually own the property but rather has a recorded long-term lease on it.</p>

                  <h3>Legal Description</h3>

                  <p class="glossary_definition">A property description, recognized by law, that is sufficient to locate and identify the property without oral testimony.</p>

                  <h3>Liabilities</h3>

                  <p class="glossary_definition">A person’s financial obligations. Liabilities include long-term and short-term debt, as well as any other amounts that are owed to others.</p>

                  <h3>Liability Insurance</h3>

                  <p class="glossary_definition">Insurance coverage that offers protection against claims alleging that a property owner’s negligence or inappropriate action resulted in bodily injury or property damage to another party.</p>

                  <h3>LIBOR-based ARMs</h3>

                  <p class="glossary_definition">The London Interbank Offered Rate (LIBOR) is based on the interest rate that major international banks are willing to lend and borrow funds for a specified period of time in the London interbank market. The LIBOR is similar to the prime-lending rate posted by major U.S. banks.</p>

                  <p class="glossary_definition">You can select an adjustable rate mortgage (ARM) that adjusts to the LIBOR at specified periods, usually every six months. This type of ARM typically has a per-adjustment period cap of 1 percent and is offered with either a 5 percent or a 6 percent lifetime rate cap.</p>

                  <h3>Lien</h3>

                  <p class="glossary_definition">A legal claim against a property that must be paid off when the property is sold.</p>

                  <h3>Lifetime Payment Cap</h3>

                  <p class="glossary_definition">For an adjustable-rate mortgage (ARM), a limit on the amount that the interest rate can increase or decrease over the life of the mortgage.</p>

                  <h3>Lifetime Rate Cap</h3>

                  <p class="glossary_definition">For an adjustable-rate mortgage (ARM), a limit on the amount that the interest rate can increase or decrease over the life of the loan.</p>

                  <h3>Line of Credit</h3>

                  <p class="glossary_definition">An agreement by a commercial bank or other financial institution to extend credit up to a certain amount for a certain time to a specified borrower.</p>

                  <h3>Liquid Asset</h3>

                  <p class="glossary_definition">A cash asset or an asset that is easily converted into cash.</p>

                  <h3>Lis Pendens</h3>

                  <p class="glossary_definition">A publicly recorded notice of a pending lawsuit against a property owner that may affect the ownership of a property. Some states require lenders to file a lis pendens to begin the foreclosure process if a borrower is in default on loan payments.</p>

                  <h3>Loan</h3>

                  <p class="glossary_definition">A sum of borrowed money (principal) that is generally repaid with interest.</p>

                  <h3>Loan Application</h3>

                  <p class="glossary_definition">The loan application is a detailed form designed to provide information from you that your lender will need. Lenders use the application to evaluate whether or not they can give you a loan, and if so, the amount of money they can lend you. The “four Cs” of credit come into play when filling out an application -- they are capacity, credit history, capital and collateral.</p>

                  <p class="glossary_definition">The loan application form requests information such as:</p>

                  <ul>
                     <li>bank account balances and account numbers, as well as bank branch address;</li>
                     <li>information about where you work or what sources of income you have;</li>
                     <li>outstanding debts (including loans and credit cards with names and addresses of creditors).</li>
                  </ul>

                  <p class="glossary_definition">Information needed for the loan application may vary from lender to lender, so prior to filling out the application it’s important to discuss with your lender what items your lender will need.</p>

                  <h3>Loan Commitment</h3>

                  <p class="glossary_definition">The commitment letter states the dollar amount of the loan being offered, the number of years you have to repay the loan, the loan origination fee, the points, the annual percentage rate, and the monthly charges.</p>

                  <p class="glossary_definition">The letter also states the time you have to accept the loan offer and to close the loan. Make sure you understand all aspects of the commitment letter because by signing it, you indicate your acceptance of its terms and conditions.</p>

                  <h3>Loan Limit</h3>

                  <p class="glossary_definition">The limit on the size of a mortgage which Fannie Mae and Freddie Mac will purchase and/or guarantee. The conforming loan limit is set annually by Fannie Mae’s and Freddie Mac’s federal regulator, The Office of Federal Housing Enterprise Oversight (OFHEO). OFHEO uses the October to October percentage increase/decrease in the average house price in the monthly interest rate survey of the Federal Housing Finance Board (FHFB) to adjust the conforming loan limits for the subsequent year.</p>

                  <p class="glossary_definition">Mortgages which exceed the conforming loan limit are known as jumbo mortgages. The interest rate on jumbo mortgages can be higher than the interest rate on conforming mortgages. A borrower whose mortgage amount slightly exceeds the conforming loan limit should analyze the economics of reducing their loan size through a larger downpayment or possibly using secondary financing to qualify for a conforming mortgage verses a jumbo mortgage.</p>

                  <h3>Loan Origination</h3>

                  <p class="glossary_definition">The process by which a mortgage lender brings into existence a mortgage secured by real property.</p>

                  <h3>Loan Origination Fee</h3>

                  <p class="glossary_definition">The loan origination fee covers the administrative costs of processing the loan. It is often expressed in points. One point is 1 percent of the mortgage amount.</p>

                  <p class="glossary_definition">For example, a $100,000 mortgage with a loan origination fee of 1 point would mean you pay $1,000.</p>

                  <h3>Loan Terms and Conditions</h3>

                  <p class="glossary_definition">With a reverse mortgage, a lender can call in your loan under certain conditions. But, if you occupy the property as your primary residence, are not absent from the property for 12 consecutive months.</p>

                  <p class="glossary_definition">You may instruct the lender to pay the taxes and insurance on your behalf from your reverse mortgage funds. The lender will set aside funds from your reverse mortgage to pay for future taxes and insurance, as long as funds are available.</p>

                  <p class="glossary_definition">Furthermore, as long as you comply with the terms noted above, you can’t be forced to sell your home to pay off the reverse mortgage, even if the loan balance grows to exceed the value of your property.</p>

                  <h3>Loan-to-Value (LTV) Percentage</h3>

                  <p class="glossary_definition">The relationship between the principal balance of the mortgage and the appraised value (or sales price if it is lower) of the property. For example, a $100,000 home with an $80,000 mortgage has a LTV percentage of 80 percent.</p>

                  <h3>Lock-In</h3>

                  <p class="glossary_definition">A written agreement in which the lender guarantees a specified interest rate if a mortgage goes to closing within a set period of time. The lock-in also usually specifies the number of points to be paid at closing.</p>

                  <h3>Lock-In Period</h3>

                  <p class="glossary_definition">The time period during which the lender has guaranteed an interest rate to a borrower.</p>

