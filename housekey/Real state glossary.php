                     <li>real estate commissions</li>
                     <li>loan fees</li>
                     <li>points</li>
                     <li>escrow amounts</li>
                  </ul>

                  <p class="glossary_definition">The form is filled out by your closing agent and must be signed by the buyer and the seller. The buyer should be allowed to review the HUD-1 Settlement Statement on the business day before the closing meeting to know the closing costs in advance.</p>

                  <p class="glossary_definition">The HUD-1 Settlement Statement is also known as the “closing statement” or “settlement sheet.”</p>

                  <h3>Short Sale</h3>

                  <p class="glossary_definition">A property sale negotiated with a mortgage company in which a lender takes less than the total amount due.</p>

                  <h3>Single-Family Properties</h3>

                  <p class="glossary_definition">One- to four-unit properties including detached homes, townhomes, condominiums, and cooperatives.</p>

                  <h3>Six-Month Adjustable-Rate Mortgage</h3>

                  <p class="glossary_definition">This adjustable-rate mortgage (ARM) offers a low initial interest rate for the first six months with an interest rate that adjusts every six months thereafter. The rate caps per adjustment can be 1 percent or 2 percent; the lifetime adjustment caps can be 4 percent, 5 percent, or 6 percent. This type of mortgage may be right for you if you anticipate a rapid increase in income over the first few years of your mortgage. That’s because it lets you maximize your purchasing power immediately. It may also be the right mortgage for you if you plan to live in your home for only a few years.</p>

                  <p class="glossary_definition">The interest rate is tied to a published financial index. When comparing ARMs that have different indexes, look at how the index has performed recently. Your an approved lender can provide information on how to track a specific index and how to review a 15-year history of the index.</p>

                  <p class="glossary_definition">Advantages:</p>

                  <ul>
                     <li>Maximizes your buying power immediately, especially if you expect your income to rise quickly in the next few years.</li>
                     <li>Lets you select an index that meets your financial needs.</li>
                     <li>Easier to qualify for due to a low interest rate and a 1 or 2 percent annual rate cap.</li>
                     <li>Some six-month ARMs let you convert to a fixed-rate loan at certain adjustment intervals. Ask your Fannie Mae approved lender which of their six-month ARMs include this option. Your lender can also provide further specifics about this mortgage option.</li>
                  </ul>

                  <p>Details:</p>

                  <ul>
                     <li>You can get a six-month ARM with a term of 10 to 30 years. Typically, they are 10, 15, or 30 years.</li>
                     <li>Can be used to buy one- to four-family, owner-occupied principal residences including second homes, investment properties, and condos, co-ops and planned unit developments.</li>
                     <li>Manufactured homes are also eligible. (Manufactured housing units must be built on a permanent chassis at a factory and then transported to a permanent site and attached to a foundation.)</li>
                  </ul>

                  <h3>Special Deposit Account</h3>

                  <p class="glossary_definition">An account that is established for rehabilitation mortgages to hold the funds needed for the rehabilitation work so they can be disbursed from time to time as particular portions of the work are completed.</p>

                  <h3>Standard Payment Calculation</h3>

                  <p>The method used to determine the monthly payment required to repay the remaining balance of a mortgage in substantially equal installments over the remaining term of the mortgage at the current interest rate.</p>

                  <h3>Step-Rate Mortgage</h3>

                  <p>A mortgage that allows for the interest rate to increase according to a specified schedule (i.e. seven years), resulting in increased payments as well. At the end of the specified period, the rate and payments will remain constant for the remainder of the loan.</p>

                  <h3>Subdivision</h3>

                  <p>A housing development that is created by dividing a tract of land into individual lots for sale or lease.</p>

                  <h3>Subordinate Financing</h3>

                  <p>Any mortgage or other lien that has a priority that is lower than that of the first mortgage.</p>

                  <h3>Subprime</h3>

                  <p class="glossary_definition">Designating a loan (typically at a greater than usual rate of interest) offered to a borrower who is not qualified for other loans (e.g. because of poor credit history).</p>

                  <h3>Subsidized Second Mortgage</h3>

                  <p class="glossary_definition">An alternative financing option known as the Community Seconds® mortgage for low- and moderate-income households. An investor purchases a first mortgage that has a subsidized second mortgage behind it. The second mortgage may be issued by a state, county, or local housing agency, foundation, or nonprofit corporation. Payment on the second mortgage is often deferred and carries a very low interest rate (or no interest rate). Part of the debt may be forgiven incrementally for each year the buyer remains in the home.</p>

                  <h3>Survey</h3>

                  <p class="glossary_definition">A drawing or map showing the precise legal boundaries of a property, the location of improvements, easements, rights of way, encroachments, and other physical features.</p>

                  <p class="glossary_definition">Your lender may require you to have a survey of the property performed. This process confirms that the property’s boundaries are correctly described in the purchase and sale agreement.</p>

                  <p class="glossary_definition">Also called a plot plan, the survey may show a neighbor’s fence is located on the seller’s property or more serious violations may be discovered. These violations must be addressed before the lender will proceed.</p>

                  <p class="glossary_definition">The buyer usually pays to have the survey done, but some cost savings may be found by requesting an “update” from the company that previously surveyed the property.</p>

                  <h3>Sweat Equity</h3>

                  <p class="glossary_definition">Contribution to the construction or rehabilitation of a property in the form of labor or services rather than cash.</p>

